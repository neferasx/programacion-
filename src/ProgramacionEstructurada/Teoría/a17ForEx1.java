package Teoría;

public class a17ForEx1 {
    public static void main(String[] args){
        // Creciente
        for(int i=1; i<11; i++){
            System.out.println("Count is: " + i);
        }
        System.out.println();
        // Decreciente
        for(int j=10; j>0; j--) {
            System.out.println("Count is: " + j);
        }
        // Nº par
        System.out.println();
        for(int k=2; k<11; k+=2) {
            System.out.println("Par is: " + k);
        }


    }
}
